import os
from langchain_openai import ChatOpenAI

from pydantic import SecretStr
from dotenv import load_dotenv
load_dotenv()

llm = ChatOpenAI( #创建了一个可以调用大模型的工具
    model="qwen-plus",#配置了要使用的模型版本、接口地址、身份密钥，还开启了流式输出功能
    base_url="https://dashscope.aliyuncs.com/compatible-mode/v1",
    api_key=SecretStr(os.getenv("DASHSCOPE_API_KEY")),
    streaming=True,
)
# 准备 参数配置
# 请求 api调用
# 接收阶段 流式返回

# 这段代码首先用 ChatOpenAI 创建了一个可以调用大模型的工具，配置了要使用的模型版本、接口地址和身份密钥，还开启了流式输出功能。
# 接下来调用这个工具向大模型提问 “解释一下流式调用”，
# 并把模型返回的内容一段一段地实时打印出来，让我们不用等完整结果生成就能看到内容。


# 流式获取单个 token
for chunk in llm.stream("解释一下流式调用"):
    # 每次拿到一个 token（chunk），立即打印（不加换行）
    print(chunk.content, end="", flush=True)



















































































































































































































































































































































































